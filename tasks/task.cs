public class Task
{
    public decimal sum = 0;

    public decimal Mod(decimal n)
    {
        if (n < 0)
        {
            return -n;
        }
        else
        {
            return n;
        }
    }

    public decimal Calculate()
    {
        public decimal res = 0;
        public decimal x = 0.56m;
        public int n = 0;

        while (Mod((Math.Pow(2, 2n) + 1)/(Math.Pow(2, n) + 1) <= Math.Pow(10, -4));
        {
            res = Math.Pow(-1, n) * ((Math.Pow(x, 2n) + 1)/(Math.Pow(2, n) + 1));
            n++;
        }
    }
}